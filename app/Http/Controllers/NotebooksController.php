    {
        $notebook = Notebook::find($id);
        $invisible = 0;
        $kotae = 1;
        
        if(isset($request->invisible)) {
            $invisible = $request->invisible;
        } 
        
        if(isset($request->kotae)) {
            $kotae = $request->kotae;
        } 
        
        return view('notebooks.show', [
            'notebook' => $notebook,
            'invisible' => $invisible,
            'kotae' => $kotae,
        ]);
    
    }

